# Operators in Python

# Addition
x = 5
y = 3
print(x + y)

# Subtraction
h = 4
print(x - h)

# Multiplication
print(y * x)

# Standard Division
e = 6
r = 2
print(e / r)
w = 7
print(w / r)

# Interger division
print(e // r)
print(w // r)

# Modulus division
print(e % r)
print(w % r)
# for the 2nd example we get 1 because it is the remainder

# Power operator
print(r ** y)